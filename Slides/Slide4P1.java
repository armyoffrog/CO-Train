
public class Slide4P1 {

    static boolean evenOdd(int number){

        if (number % 2 == 0){

            return true;

        } else {

            return false;

        }

    }

    public static void main(String[] args){

        System.out.println(evenOdd(10));
        System.out.println(evenOdd(3));
        System.out.println(evenOdd(6));

    }

} 
